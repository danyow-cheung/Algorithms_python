import collections


class Solution(object):
    def countCharacters(self, words, chars):
        """
        :type words: List[str]
        :type chars: str
        :rtype: int
        """
        count = collections.Counter(chars)
        print(count)
        res = 0
        for i in words:
            count_re = count.copy()
            cur_len = len(i)
            ans = 0
            for j in i:
                if count_re[j]>0:
                    count_re[j]-=1
                    ans+=1
            if ans==cur_len:
                res+=cur_len
        print('res',res)
        return res


