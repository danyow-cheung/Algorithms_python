'''问题描述
一个口袋放12个球，三个红的三个白的，六个黑的
从中任取8个会有多少种可能颜色搭配
'''

'''问题分析
设红球m个，白球n个，黑球8-m-n个
满足条件有m[0,3],n[0,3]黑球的总数为6个所以8-m-n<=6
'''

'''算法设计
循环m和n的所有取值，然后再找满足8-m-n<=6的组合'''
# 多少种方案
num = 0
m = 0
while m <= 3:
    n = 0
    while n<=3:
        if 8-m-n <= 6:
            num += 1
            print("%2d: %d  \t\t %d \t\t %d"%(num,m,n,8-m-n))
        n+= 1
    m+= 1
print("总方案有",num)
