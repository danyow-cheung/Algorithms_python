'''問題描述【換分幣】
將5元兌換成1元，5角和1角的硬幣，共有多少種不同的兌換方法
'''
'''問題分析
使用循環'''

count = 0 
# x 為兌換1元硬幣錢數，可能取值為{0,10,20,30,40,50}
for x in range(0,50+1,10):
    # y為兌換5角硬幣的錢數，取值為{0,5,10,15,20,25,30,35,40,45,50}
    for y in range(0,50-x+1,5):
        #z 為兌換1角硬幣錢數，取值為{0,1,...50}
        for z in range(0,50-x-y+1,1):
            if x+y+z ==50:
                count+= 1
                print("10*%d+5*%d+1*%d \t" % (x // 10, y // 5, z),end=" ")