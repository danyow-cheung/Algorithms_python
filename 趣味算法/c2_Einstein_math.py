'''问题描述
一条长阶梯，每跨两阶剩一阶  每跨三阶剩二阶 每跨五步剩四阶 跨六剩五 
跨七阶才正好一阶不剩
请问在1到n内，有多少个数能满足
'''

'''问题分析
用x表示阶梯数满足条件有
x%2=1
x%3=2
x%5=4
x%6=5
x%7=0'''

n = int(input("请输入n"))
sum = 0 
i = 7 
while i < n+1:
    if (i%7==0) and (i%6==5)and(i%5==4)and(i%3==2):
        sum += 1 
        print(i)
    i+= 1
print(f"n={n},sum={sum}")