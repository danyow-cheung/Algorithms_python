'''問題描述【奇數平方的有趣性質】
任意奇数的平方有这样的有趣性质:
任意奇数的平方与1的差是8的倍数。
要求编程验证奇数的这个性质。
'''

if __name__ == "__main__":
    # 对1000~2000范围内的所有奇数进行验证 
    n = 1001
    while n <= 1999:
        # 输出当前的奇数
        print("%d: " %(n), end=" ") 
        print("(%d*%d-1)/8" %(n, n), end=" ") # 输出(n*n-1)/8的值
        print("= %d" %((n*n-1)//8), end=" ") # 输出(n*n-1)/8的余数
        print("+ %d\n" %((n*n-1)%8), end=" ") 
        n += 2