'''問題描述【高次方尾數】
求13的13次方的最後三位數
'''

'''問題分析
沒辦法乘之後求因為計算機有最大範圍限制
'''

'''算法分析
乘积的最后三位的值只与乘数和被乘数的后三位有关，与乘数和被乘数的高位无关
求某數的後三位直接用該數對1000取模
'''
if __name__ =="__main__":
    # 變量last保存所求得的x的y次方的部分積的後三位
    last=  1
    print("請輸入兩個數x和y")
    x = int(input("x = "))
    y = int(input("y = "))
    for i in range(1,y+1):
        # last 乘x後對1000取模，既求積的後三位
        last = last * x %1000
    print("%d的%d次方所得積的後三位為%d"%(x,y,last))